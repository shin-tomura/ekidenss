// ----------------------------------------------------
// 2. ビットパッキング処理クラス
// ----------------------------------------------------

/// 趣味インデックス (最大2047まで: 11ビット) と
/// 都道府県インデックス (最大63まで: 6ビット) を格納するためのヘルパークラス。
class PackedIndexHelper {
  // 都道府県インデックス用のビット数とマスク
  static const int _prefBits = 6; // 2^6 = 64 (47種類をカバー)
  static const int _prefMask = (1 << _prefBits) - 1; // 0x3F (6個の1)

  // 趣味インデックス用のビット数とマスク
  static const int _hobbyBits = 11; // 2^11 = 2048 (最大2000要素をカバー)
  static const int _hobbyMask = (1 << _hobbyBits) - 1; // 0x7FF (11個の1)

  // 趣味インデックスが始まるシフト位置 (都道府県インデックスのビット数の後)
  static const int _hobbyShift = _prefBits; // 6

  /*
   * 2つのインデックスを1つのint型変数に格納（パック）します。
   * 都道府県インデックスが下位6ビット、趣味インデックスがそれに続く11ビットに格納されます。
   */
  static int packIndices({
    required int hobbyIndex,
    required int prefectureIndex,
  }) {
    // 範囲チェック (安全のため)
    if (prefectureIndex > _prefMask) {
      throw RangeError('都道府県インデックスが最大値($_prefMask)を超えています');
    }
    if (hobbyIndex > _hobbyMask) {
      throw RangeError('趣味インデックスが最大値($_hobbyMask)を超えています');
    }

    // 1. 都道府県インデックス (下位6ビット)
    int packed = prefectureIndex;

    // 2. 趣味インデックスを6ビット左にシフトし、OR演算で結合（ビット6～16に格納）
    packed |= (hobbyIndex << _hobbyShift);

    return packed;
  }

  /*
   * 1つのint型変数から2つのインデックスを取り出し（アンパック）ます。
   * 戻り値は取り出した値を格納したMapです。
   */
  static Map<String, int> unpackIndices(int packedValue) {
    // 1. 都道府県インデックスの取り出し (ビット0～5)
    // ビットマスク0x3F (& _prefMask) を適用して下位6ビットのみを取り出す
    final int prefectureIndex = packedValue & _prefMask;

    // 2. 趣味インデックスの取り出し (ビット6～16)
    // 最初に6ビット右にシフトし、ビット6～16をビット0～10に移動させる
    // その後、ビットマスク0x7FF (& _hobbyMask) を適用して11ビット分のみを取り出す
    final int hobbyIndex = (packedValue >> _hobbyShift) & _hobbyMask;

    return {'hobbyIndex': hobbyIndex, 'prefectureIndex': prefectureIndex};
  }
}

