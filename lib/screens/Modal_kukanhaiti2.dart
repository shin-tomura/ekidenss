  Widget _buildUnivSelector(List<UnivData> entryUnivs) {
    if (entryUnivs.isEmpty) return const SizedBox.shrink();
    final currentIndex = entryUnivs.indexWhere((u) => u.id == _selectedUnivId);

    return Container(
      padding: const EdgeInsets.symmetric(horizontal: 12, vertical: 8),
      child: Row(
        children: [
          IconButton(
            icon: const Icon(
              Icons.arrow_back_ios_new,
              color: Colors.cyanAccent,
              size: 20,
            ),
            onPressed: currentIndex > 0
                ? () => setState(
                    () => _selectedUnivId = entryUnivs[currentIndex - 1].id,
                  )
                : () => setState(
                    () =>
                        _selectedUnivId = entryUnivs[entryUnivs.length - 1].id,
                  ),
          ),
          Expanded(
            child: Container(
              padding: const EdgeInsets.symmetric(horizontal: 16),
              decoration: BoxDecoration(
                color: Colors.white.withOpacity(0.1),
                borderRadius: BorderRadius.circular(30),
                border: Border.all(color: Colors.white30),
              ),
              child: DropdownButtonHideUnderline(
                child: DropdownButton<int>(
                  value: _selectedUnivId,
                  dropdownColor: Colors.grey[900],
                  isExpanded: true,
                  icon: const Icon(Icons.swap_vert, color: Colors.cyanAccent),
                  items: entryUnivs
                      .map(
                        (u) => DropdownMenuItem(
                          value: u.id,
                          child: Text(
                            u.name,
                            style: const TextStyle(
                              color: Colors.white,
                              fontSize: 16,
                              fontWeight: FontWeight.w600,
                            ),
                          ),
                        ),
                      )
                      .toList(),
                  onChanged: (val) => setState(() => _selectedUnivId = val),
                ),
              ),
            ),
          ),
          IconButton(
            icon: const Icon(
              Icons.arrow_forward_ios,
              color: Colors.cyanAccent,
              size: 20,
            ),
            onPressed: currentIndex < entryUnivs.length - 1
                ? () => setState(
                    () => _selectedUnivId = entryUnivs[currentIndex + 1].id,
                  )
                : () => setState(() => _selectedUnivId = entryUnivs[0].id),
          ),
        ],
      ),
    );
  }

  Widget _buildMainList(Ghensuu gh, List<SenshuData> senshus) {
    final int raceIdx = gh.hyojiracebangou;
    final int kukanCount = gh.kukansuu_taikaigoto[raceIdx];

    return ListView.builder(
      padding: const EdgeInsets.symmetric(horizontal: 12, vertical: 8),
      itemCount: kukanCount,
      itemBuilder: (context, i_kukan) {
        final targetSenshus = senshus
            .where((s) => s.entrykukan_race[raceIdx][s.gakunen - 1] == i_kukan)
            .toList();
        if (targetSenshus.isEmpty) return const SizedBox.shrink();
        return Column(
          children: targetSenshus
              .map((s) => _buildSenshuCard(gh, i_kukan, s))
              .toList(),
        );
      },
    );
  }

