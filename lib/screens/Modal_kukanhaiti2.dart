  void _showSenshuDetail(BuildContext context, int senshuId) {
    showGeneralDialog(
      context: context,
      barrierColor: Colors.black.withOpacity(0.8),
      barrierDismissible: true,
      barrierLabel: '詳細',
      transitionDuration: const Duration(milliseconds: 300),
      pageBuilder: (context, animation, secondaryAnimation) =>
          ModalSenshuDetailView(senshuId: senshuId),
      transitionBuilder: (context, animation, secondaryAnimation, child) =>
          FadeTransition(
            opacity: CurvedAnimation(parent: animation, curve: Curves.easeOut),
            child: child,
          ),
    );
  }

  Widget _buildNouryokuSection(Ghensuu gh, SenshuData s) {
    //過去に同じ区間を走ったことがあるかどうかの表示
    int keikenkaisuu = 0;
    for (int i_gakunen = 0; i_gakunen < s.gakunen - 1; i_gakunen++) {
      if (s.entrykukan_race[gh.hyojiracebangou][i_gakunen] ==
          s.entrykukan_race[gh.hyojiracebangou][s.gakunen - 1]) {
        keikenkaisuu++;
      }
    }
    return Padding(
      padding: const EdgeInsets.only(bottom: 12),
      child: Wrap(
        spacing: 12, // アイテム間の横の隙間
        runSpacing: 8, // 折り返した時の行間の隙間
        children: [
          _nouryokuItem(
            "駅伝男",
            gh.nouryokumieruflag[0] == 1 ? "${s.konjou}" : "??",
          ),
          _nouryokuItem(
            "平常心",
            gh.nouryokumieruflag[1] == 1 ? "${s.heijousin}" : "??",
          ),
          _nouryokuItem("調子", "${s.chousi}"),
          if (keikenkaisuu > 0) _nouryokuItem("この区間の経験回数", "$keikenkaisuu"),
        ],
      ),
    );
  }

  // --- 各アイテムの定義 ---
  Widget _nouryokuItem(String label, String value) {
