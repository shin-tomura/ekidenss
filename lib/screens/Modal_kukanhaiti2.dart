class ModalKukanHaitiView extends StatefulWidget {
  final int targetUnivid;
  const ModalKukanHaitiView({super.key, required this.targetUnivid});

  @override
  State<ModalKukanHaitiView> createState() => _ModalKukanHaitiViewState();
}

class _ModalKukanHaitiViewState extends State<ModalKukanHaitiView> {
  final ScreenshotController _screenshotController = ScreenshotController();
  int? _selectedUnivId;
  bool _isExporting = false;

  String _formatDoubleToFixed(double value, int fractionDigits) {
    return value.toStringAsFixed(fractionDigits);
  }

  // 画像出力処理
  Future<void> _exportAsImage({required bool showDetail}) async {
    setState(() => _isExporting = true);
    try {
      final gBox = Hive.box<Ghensuu>('ghensuuBox');
      final sBox = Hive.box<SenshuData>('senshuBox');
      final uBox = Hive.box<UnivData>('univBox');

      final currentGhensuu = gBox.getAt(0)!;
      final univ = uBox.values.firstWhere((u) => u.id == _selectedUnivId);
      final raceIdx = currentGhensuu.hyojiracebangou;
      final kukanCount = currentGhensuu.kukansuu_taikaigoto[raceIdx];
      final allSenshus = sBox.values
          .where((s) => s.univid == _selectedUnivId)
          .toList();

      // レイアウト計算
      double canvasWidth = showDetail ? 1500.0 : 800.0;
      int itemsPerRow = showDetail ? 4 : 2;
      int rowCount = (kukanCount + itemsPerRow - 1) ~/ itemsPerRow;
      if (raceIdx == 3) {
        rowCount = (kukanCount * 2 + itemsPerRow - 1) ~/ itemsPerRow;
      }
      //double itemHeight = showDetail ? 650.0 : 100.0;
      double itemHeight = showDetail ? 420.0 : 70.0;
      double finalHeight = 150.0 + (rowCount * itemHeight) + 100.0;

      Widget captureWidget = Container(
        width: canvasWidth,
        height: finalHeight,
        padding: const EdgeInsets.all(40),
        color: HENSUU.backgroundcolor,
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Text(
              '${univ.name} - 区間配置 (${showDetail ? "詳細" : "標準"})',
              style: const TextStyle(
                color: Colors.white,
                fontSize: 32,
                fontWeight: FontWeight.bold,
                decoration: TextDecoration.none,
              ),
            ),
            const SizedBox(height: 10),
            Divider(color: Colors.white.withOpacity(0.5), thickness: 2),
            const SizedBox(height: 20),
            Expanded(
              child: Wrap(
                spacing: 20,
                runSpacing: 20,
                children: List.generate(kukanCount, (i) {
                  final targetSenshus = allSenshus
                      .where(
                        (s) => s.entrykukan_race[raceIdx][s.gakunen - 1] == i,
                      )
                      .toList();
                  if (targetSenshus.isEmpty) return const SizedBox.shrink();

                  return SizedBox(
                    //width: (canvasWidth - 120) / itemsPerRow,
                    width: 340,
                    child: Column(
                      children: targetSenshus
                          .map(
                            (s) => _buildSenshuPanelForImage(
                              currentGhensuu,
                              i,
                              s,
                              showDetail,
                            ),
                          )
                          .toList(),
                    ),
                  );
                }),
              ),
            ),
            const Align(
              alignment: Alignment.centerRight,
              child: Text(
                '----- Generated by 箱庭小駅伝SS -----',
                style: TextStyle(
                  color: Colors.grey,
                  fontSize: 16,
                  decoration: TextDecoration.none,
                ),
              ),
            ),
          ],
        ),
      );

      final image = await _screenshotController.captureFromWidget(
        MediaQuery(
          data: const MediaQueryData(textScaler: TextScaler.noScaling),
          child: Directionality(
            textDirection: TextDirection.ltr,
            child: Material(child: captureWidget),
          ),
        ),
        targetSize: Size(canvasWidth, finalHeight),
      );

      final directory = await getTemporaryDirectory();
      final file = File('${directory.path}/kukan_haiti.png');
      await file.writeAsBytes(image!);
      await SharePlus.instance.share(
        ShareParams(files: [XFile(file.path)], text: '${univ.name} 区間配置'),
      );
    } catch (e) {
      debugPrint("Export Error: $e");
    } finally {
      setState(() => _isExporting = false);
    }
  }

  // 画像出力専用のパネル（ExpansionTileを使わずフラットに描画）
  Widget _buildSenshuPanelForImage(
    Ghensuu gh,
    int kukanIdx,
    SenshuData s,
    bool showDetail,
  ) {
    String kukanLabel = "${kukanIdx + 1}区";
    if (gh.hyojiracebangou == 3) kukanLabel = "${kukanIdx + 1}組";
    if (gh.hyojiracebangou == 4) kukanLabel = "記録会";

    return Container(
      margin: const EdgeInsets.only(bottom: 10),
      padding: const EdgeInsets.all(15),
      decoration: BoxDecoration(
        color: Colors.white.withOpacity(0.1),
        borderRadius: BorderRadius.circular(10),
        border: Border.all(color: Colors.white24),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Row(
            children: [
              Text(
                kukanLabel,
                style: const TextStyle(
                  color: Colors.cyanAccent,
                  fontWeight: FontWeight.bold,
                  fontSize: 16,
                  decoration: TextDecoration.none,
                ),
              ),
              const SizedBox(width: 10),
              Text(
                s.name,
                style: const TextStyle(
                  color: Colors.white,
                  fontWeight: FontWeight.bold,
                  fontSize: 18,
                  decoration: TextDecoration.none,
                ),
              ),
              //const Spacer(),
              Text(
                " (${s.gakunen}年)",
                style: const TextStyle(
                  color: Colors.white,
                  fontSize: 16,
                  decoration: TextDecoration.none,
                ),
              ),
            ],
          ),
          if (showDetail) ...[
            const Padding(
              padding: EdgeInsets.symmetric(vertical: 8),
              child: Divider(color: Colors.white24),
            ),
            _buildNouryokuSection(gh, s),
            _buildRecordGrid(gh, s),
          ],
        ],
      ),
    );
  }

  @override
  Widget build(BuildContext context) {
    final Box<Ghensuu> ghensuuBox = Hive.box<Ghensuu>('ghensuuBox');
    final Box<UnivData> univdataBox = Hive.box<UnivData>('univBox');
    final Box<SenshuData> senshudataBox = Hive.box<SenshuData>('senshuBox');

    return ValueListenableBuilder<Box<Ghensuu>>(
      valueListenable: ghensuuBox.listenable(),
      builder: (context, gBox, _) {
        final Ghensuu? currentGhensuu = gBox.getAt(0);
        if (currentGhensuu == null)
          return const Scaffold(body: Center(child: Text("データエラー")));

        if (_selectedUnivId == null) {
          _selectedUnivId = widget.targetUnivid;
        }

        final List<UnivData> allUnivs = univdataBox.values.toList()
          ..sort((a, b) => a.id.compareTo(b.id));
        bool selectedUnivIdEntryflag = false;
        if (allUnivs[_selectedUnivId!].taikaientryflag[currentGhensuu
                .hyojiracebangou] ==
            1) {
          selectedUnivIdEntryflag = true;
        }
        if (selectedUnivIdEntryflag == false) {
          for (int i = 0; i < allUnivs.length; i++) {
            if (allUnivs[i].taikaientryflag[currentGhensuu.hyojiracebangou] ==
                1) {
              _selectedUnivId = i;
              break;
            }
          }
        }

        return ValueListenableBuilder<Box<UnivData>>(
          valueListenable: univdataBox.listenable(),
          builder: (context, uBox, _) {
            final List<UnivData> entryUnivs =
                uBox.values
                    .where(
                      (u) =>
                          u.taikaientryflag[currentGhensuu.hyojiracebangou] ==
                          1,
                    )
                    .toList()
                  ..sort((a, b) => a.id.compareTo(b.id));

            return ValueListenableBuilder<Box<SenshuData>>(
              valueListenable: senshudataBox.listenable(),
              builder: (context, sBox, _) {
                final allSenshuData = sBox.values.toList();
                final displaySenshuData = allSenshuData
                    .where((s) => s.univid == _selectedUnivId)
                    .toList();

                if (currentGhensuu.hyojiracebangou == 4) {
                  displaySenshuData.sort((a, b) {
                    final int gakunenComp = b.gakunen.compareTo(a.gakunen);
                    return gakunenComp != 0
                        ? gakunenComp
                        : a.id.compareTo(b.id);
                  });
                }

                return Stack(
                  children: [
                    Scaffold(
                      backgroundColor: HENSUU.backgroundcolor,
                      appBar: AppBar(
                        title: const Text(
                          '区間配置確認',
                          style: TextStyle(
                            fontWeight: FontWeight.bold,
                            color: Colors.white,
                          ),
                        ),
                        backgroundColor: Colors.transparent,
                        elevation: 0,
                        centerTitle: true,
                        iconTheme: const IconThemeData(color: Colors.white),
                        actions: [
                          IconButton(
                            icon: const Icon(Icons.share),
                            onPressed: () => _showExportMenu(context),
                          ),
                        ],
                      ),
                      body: Column(
                        children: [
                          _buildUnivSelector(entryUnivs),
                          const Divider(color: Colors.white24, height: 1),
                          Expanded(
                            child: _buildMainList(
                              currentGhensuu,
                              displaySenshuData,
                            ),
                          ),
                          _buildBottomBar(context),
                        ],
                      ),
                    ),
                    if (_isExporting)
                      Container(
                        color: Colors.black54,
                        child: const Center(child: CircularProgressIndicator()),
                      ),
                  ],
                );
              },
            );
          },
        );
      },
    );
  }

