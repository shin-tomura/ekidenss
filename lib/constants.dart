/// 強化練習メニューの定数と文字列変換を管理する静的クラス
class TrainingMenu {
  //static int plusryou = 28;
  //static int plusryou_balance = 4;
  // ----------------------------------------------------
  // 練習メニューの定義
  // kaifukuryokuの値 (int) と表示文字列 (String) のマッピング
  // ----------------------------------------------------
  static const Map<int, String> menuOptions = {
    0: 'バランス', // 平均的に練習
    1: 'スピード', // スパート力とペース変動対応力を強化
    2: '距離走', // 長距離粘りとロード適性を強化
    3: '登り', // 登り適性を強化
    4: '下り', // 下り適性を強化
    5: 'アップダウン', // アップダウン対応力を強化
  };

  /// 練習メニューのID (0-5) から対応する文字列を取得します。
  ///
  /// 指定された ID が範囲外 (0〜5以外) の場合は '不明' または 'バランス' を返します。
  /// @param menuId senshu.kaifukuryoku に格納されている値 (0〜5)
  /// @return 対応する練習メニューの文字列
  static String getMenuString(int menuId) {
    // menuId が Map のキーに含まれているかチェックし、あれば対応する文字列を返す
    // 含まれていない場合は、安全のため 'バランス' (0) の文字列を返す
    return menuOptions[menuId] ?? 'バランス';
  }

  /// 練習メニューのID (0-5) から、メニューの簡単な説明を取得します。
  ///
  /// @param menuId senshu.kaifukuryoku に格納されている値 (0〜5)
  /// @return 対応するメニューの説明文字列
  static String getMenuDescription(int menuId) {
    switch (menuId) {
      case 0:
        return '平均的に練習する';
      case 1:
        return 'スパート力とペース変動対応力を強化';
      case 2:
        return '長距離粘りとロード適性を強化';
      case 3:
        return '登り適性を強化';
      case 4:
        return '下り適性を強化';
      case 5:
        return 'アップダウン対応力を強化';
      default:
        // 範囲外の数値が設定されていた場合
        return '設定なし (デフォルト)';
    }
  }
}

